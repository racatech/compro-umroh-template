function countUp(el, raw, duration = 1800) {
    const suffix = raw.replace(/[\d.,]/g, "");
    const numStr = raw.replace(/[^\d.]/g, "");
    const target = parseFloat(numStr);
    const isFloat = numStr.includes(".");
    const hasComma = raw.includes(",") || raw.includes(".");
    const start = performance.now();

    const tick = (now) => {
        const p = Math.min((now - start) / duration, 1);
        const ease = 1 - Math.pow(1 - p, 3);
        const val = target * ease;
        let display = isFloat ? val.toFixed(1) : Math.floor(val).toString();
        if (hasComma && !isFloat && display.length > 3) {
            display = Math.floor(val).toLocaleString("id-ID");
        }
        el.textContent = display + suffix;
        if (p < 1) requestAnimationFrame(tick);
        else el.textContent = raw;
    };
    requestAnimationFrame(tick);
}

